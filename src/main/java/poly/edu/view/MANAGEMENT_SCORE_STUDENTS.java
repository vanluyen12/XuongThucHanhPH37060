    public void tinhdiemTB() {
        if (txtTiengAnh.equals("") || txtTinHoc.equals("") || txtGDTC.equals("")) {
            return;
        }
        float tienganh = Float.parseFloat(txtTiengAnh.getText());
        float tinhoc = Float.parseFloat(txtTinHoc.getText());
        float tgdtc = Float.parseFloat(txtGDTC.getText());
        float ketqua = (tienganh + tinhoc + tgdtc) / 3;
        String formattedKetQua = String.format("%.1f", ketqua); // Format ketqua to have two decimal places
        txtDiemTrungBinh.setText(formattedKetQua);

    }

