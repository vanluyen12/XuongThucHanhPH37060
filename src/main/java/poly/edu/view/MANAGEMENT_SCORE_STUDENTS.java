    private void LoadToTable() throws Exception {
        model.setRowCount(0);
        ArrayList<GRADES> list = gds.findByTop3(5);
        for (GRADES grades : list) {
            float diemtb = (grades.getTiengAnh() + grades.getTinHoc() + grades.getGDTC()) / 3;
            String format = String.format("%.1f", diemtb);
            model.addRow(new Object[]{
                grades.getId(), grades.getMasSV(), grades.getTiengAnh(),
                grades.getTinHoc(), grades.getGDTC(), diemtb
            });
        }
        String[] data = {
            "ID", "MÃ SINH VIÊN ", "TIẾNG ANH", "TIN HỌC", "GDTC", "DIEM TB"
        };
        model.setColumnIdentifiers(data);
    }

