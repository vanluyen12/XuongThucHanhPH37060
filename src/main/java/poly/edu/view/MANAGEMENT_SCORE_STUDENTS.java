    private void btnSaveDSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveDSVActionPerformed
        try {
            if (checkEmty()) {
                return;
            }
            GRADES d = GradeMethod();

            if (gds.findByid(txtMaSV.getText()) != null) {
                int chon = JOptionPane.showConfirmDialog(this, "Sinh viên đã tồn tại , Bạn có muốn update không ? ", "Thông báo", JOptionPane.YES_NO_OPTION);
                if (chon != JOptionPane.YES_OPTION) {
                    return;
                } else {
                    if (gds.upDateDiemSinhVien(d)) {
                        JOptionPane.showMessageDialog(this, "Update thành công !");
                        LoadToTable();
                        return;
                    } else {
                        JOptionPane.showMessageDialog(this, "Update thất bại !");
                        return;
                    }
                }
            } else {
                if (gds.themDiemSinhVien(d)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công !");
                    LoadToTable();
                    return;
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại  !");
                    return;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();

        }
    }//GEN-LAST:event_btnSaveDSVActionPerformed

